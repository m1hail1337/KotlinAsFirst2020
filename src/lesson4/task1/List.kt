 * Римские цифры: 1 = I, 4 = IV, 5 = V, 9 = IX, 10 = X, 40 = XL, 50 = L,
 * 90 = XC, 100 = C, 400 = CD, 500 = D, 900 = CM, 1000 = M.
 * Например: 23 = XXIII, 44 = XLIV, 100 = C
 */

fun roman(n: Int): String {
    //val romanChars = listOf("I", "V", "X", "L", "C", "D", "M")
    val ans = mutableListOf<String>()
    fun constructor(k: Int, arg: String) {
        for (i in 0 until k)
            ans.add(ans.size, arg)

    }

    val m1 = n / 1000
    constructor(m1, "M")
    var m2 = n % 1000
    if (m2 / 100 == 9) {
        ans.add(ans.size, "CM")
        m2 = -1
    } else if (m2 / 100 == 4) {
        ans.add(ans.size, "CM")
        m2 = -1
    }

    val d1 = m2 / 500
    constructor(d1, "D")
    val d2 = m2 % 500
    val c1 = d2 / 100
    constructor(c1, "C")
    var c2 = n % 100
    if (c2 / 10 == 9) {
        ans.add(ans.size, "XC")
        c2 = -1
    } else if (c2 / 10 == 4) {
        ans.add(ans.size, "XL")
        c2 = -1
    }
    val l1 = c2 / 50
    constructor(l1, "L")
    val l2 = c2 % 50
    val x1 = l2 / 10
    constructor(x1, "X")
    var x2 = n % 10
    if (x2 % 10 == 9) {
        ans.add(ans.size, "IX")
        x2 = -1
    } else if (x2 % 10 == 4) {
        ans.add(ans.size, "IV")
        x2 = -1
    }
    val v1 = x2 / 5
    constructor(v1, "V")
    constructor(x2 % 5, "I")
    return ans.joinToString("")
}

